package com.assettagging.test.view.assetlink;

public class UniversalSpinner {
    String id, name;

    public UniversalSpinner(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
